package day2;

public class nestedwhileloop {

	public static void main(String[] args) {
		// nested while loop
		// Nested While loop
		
				int i=0, j=0;
				
				while(++i<=5)
				{
					
					while(++j<=5)
					{
						System.out.println(j);
						
					}
					System.out.println(j);
					
				}	

	}

}
