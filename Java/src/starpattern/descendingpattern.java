package starpattern;

public class descendingpattern {  //Right angle triangle star pattern

	public static void main(String[] args) {
		
		int i ,k;
		    
		for(i=1;i<=5;i++)
		{
		
			
			for(k=1;k<=i;k++)
				{
					System.out.print("* ");
		       }
			
          System.out.println();
		}
	}

}
